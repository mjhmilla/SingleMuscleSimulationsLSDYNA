%
%  'lambdaECM_unit',  nan         
%
%  'beta1AHN_unit' ,  dl1N/dt = (F2N-F1N)/beta,                 Eqn. 18 of Millard, Franklin, Herzog
%            unit(beta) = unit(F2N-F1N)/unit(dl1N/dt)
%            unit(beta) = (F/F - F/F)/((L/T)/L)
%            unit(beta) = (-)/(1/T)
%                       = Time
%
%  'beta1PHN_unit' ,  Time (as in beta1AHN_unit)             
%
%
% Eqn 15 of Millard, Franklin, Herzog
%
%	fMN = a*fLN*(kxN*lxN+bxN*vxN) + f2N + fecmN + betaN*vM
%
%  'kxOptIsoN_unit',  
%
%
%  'dxOptIsoN_unit',  '(N/N)/((m/s)/(m/s))             = -'            Eqn. 15
%  						: dimensionless
%  'gainTrack_pos_unit',  '((m/s^2)/(m/s))/(m/m)       = 1/s'          Eqn. 16  
%						: 1/Time
%  'gainTrack_vel_unit',  '((m/s^2)/(m/s))/((m/s)/(m/s)= 1/s'          Eqn. 16  
%  						: 1/Time
%
%  'vceMax_unit'   ,  '(m/s)/m                         = 1/s'          
%                  		: 1/Time
%  'taudvst_unit'  ,   Eqn. 16
%
%        
%  'dvsBeta_unit'  ,  '((m/s^2)/(m/s) / ((m/s)/(m/s))  = 1/s'          Eqn. 16
%  'aniType_unit'  ,  nan
%  'ctlType_unit' ,  nan
%  'ctlDelay_unit',  s          
%  'ctlThrsh_unit',  nan       
%  'output_unit'   ,  nan
%  'dtout_unit'    ,  nan

%     'mid'        'ro'        'mt'       'lmc'       'nhv'    'iortho'  'ibulkIdx'     'igIdx'
%   'ivect'     'ifail'    'itherm'    'ihyper'      'ieos'      'lmca'    'unused'    'unused'
%  'stimId' 'exDefault'    'tauAct'  'tauDeact' 'fceOptIso'    'lceOpt' 'alphaOptD' 'useRgdTdn'
%   'ltSlk'     'etIso'       'dtN'  'scalePEE' 'lambdaECM'  'lPevkPtN' 'lceHNLb1A'   'act1AHN'
%'beta1AHN'  'beta1PHN' 'kxOptIsoN' 'dxOptIsoN' 'gainTrack'  'actTrack'    'vceMax'   'taudvst'
% 'dvsBeta'    'dtNumN'   'aniType'   'ctlType'  'ctlDelay'  'ctlThrsh' 'ctlOnTime'    'output'
%   'dtout'     'IBULK'        'IG'   

