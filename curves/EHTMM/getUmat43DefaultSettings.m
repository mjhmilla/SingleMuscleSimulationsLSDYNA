	case 1
		%Units of kN, mm, ms
		umat43Default = struct(        ...
			    'mid'      ,  1       ,... 
				'ro'       ,  1.e-6   ,...   
				'mt'       ,  43      ,...   
				'lmc'      ,  34      ,...    
				'nhv'      ,  1000    ,...       
				'iortho'   ,  0       ,...    
				'ibulkIdx' ,  29      ,...    
				'igIdx'    ,  30      ,...    
				'ivect'    ,  0       ,...    
				'ifail'    ,  0       ,...   
				'itherm'   ,  0       ,...    
				'ihyper'   ,  0       ,...    
				'ieos'     ,  0       ,...    
				'lmca'     ,  0       ,...    
				'unused0'   ,  ''     ,...     
				'unused1'   ,  ''     ,...     
					'stimId'   ,  nan     ,...  
					'exDefault',  0       ,...  				    
					'tauAct'   ,  10      ,...     
					'tauDeact' ,  40      ,...     
					'fceOptIso',  nan     ,...      
