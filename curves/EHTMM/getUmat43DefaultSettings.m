				'ieos'     ,  0     ,...     
				'lmca'     ,  0     ,...     
				'unused0'   ,  ''    ,...      
				'unused1'   ,  ''    ,...      
					'stimId'   ,  nan   ,...     
					'exDefault',  0     ,...  
					'tauAct'   ,  0.01  ,...        
					'tauDeact' ,  0.04  ,...        
