%  'unused_unit'   ,  nan
%  'stimId_unit'   ,  nan
%  'tauAct_unit'   ,  From Millard et al. 2012 Eqn. 2, with amin=0
%			da/dt	=     (e-a)/tau, 
%       	 tau 	=     (e-a)/(da/dt)
% 		unit(tau)	=unit((e-a)/(da/dt)), 
%                   = Time
%  'tauDeact_unit'  = Time
%  'fceOptIso_unit',  F
%  'lceOpt_unit'   ,  D
%  'alphaOptD_unit',  nan           
%  'ltSlk_unit'    ,  D
%  'etIso_unit'    ,  nan        
%  'dtN_unit'      ,  ftN = fteN(ltN) + U*kteN(ltN)*vTN
