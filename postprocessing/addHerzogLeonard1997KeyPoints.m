%          'x','Color',expColor,...
%          'MarkerFaceColor',expColor,...
%          'DisplayName',labelSeries);
end
if(addPassive0AddActive1==0)
    subplot('Position',subplotFpe);

    plot(dataHL1997.l(2),dataHL1997.fpe(2),...
         'x','Color',expColor,...
         'MarkerFaceColor',expColor,...
         'HandleVisibility','on',...
         'DisplayName',labelSeries);
    hold on;
    
